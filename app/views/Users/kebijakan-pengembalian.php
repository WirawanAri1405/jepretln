            <article class="prose lg:prose-xl max-w-none text-gray-700">
                <p>Kami berterima kasih atas kepercayaan Anda menyewa di Jepretin. Untuk menjaga kualitas layanan dan peralatan, kami memberlakukan kebijakan berikut yang wajib dipatuhi oleh setiap penyewa.</p>
                
                <h2>1. Waktu Pengembalian</h2>
                <p>Peralatan wajib dikembalikan sesuai dengan tanggal dan jam yang tertera pada nota penyewaan. Durasi sewa dihitung per 24 jam.</p>

                <h2>2. Kondisi Peralatan Saat Dikembalikan</h2>
                <ul>
                    <li>Peralatan harus dikembalikan dalam kondisi yang sama seperti saat disewa: bersih, fungsional, dan tidak ada kerusakan baru.</li>
                    <li>Semua aksesoris yang disertakan (baterai, charger, tutup lensa, tas, dll.) harus lengkap.</li>
                </ul>

                <h2>3. Denda Keterlambatan</h2>
                <p>Keterlambatan pengembalian akan dikenakan denda dengan skema sebagai berikut:</p>
                <ul>
                    <li><strong>Terlambat 1 - 6 Jam:</strong> Denda sebesar 25% dari biaya sewa harian.</li>
                    <li><strong>Terlambat 6 - 12 Jam:</strong> Denda sebesar 50% dari biaya sewa harian.</li>
                    <li><strong>Terlambat lebih dari 12 Jam:</strong> Denda dihitung sebagai biaya sewa tambahan 1 hari penuh.</li>
                </ul>

                <h2>4. Denda Kerusakan atau Kehilangan</h2>
                <p>Penyewa bertanggung jawab penuh atas segala bentuk kerusakan atau kehilangan peralatan selama masa sewa.</p>
                <ul>
                    <li><strong>Kerusakan Ringan:</strong> (Contoh: goresan kecil pada bodi) Akan dikenakan biaya perbaikan yang ditentukan oleh tim Jepretin.</li>
                    <li><strong>Kerusakan Berat:</strong> (Contoh: lensa pecah, sensor error, kerusakan akibat air) Penyewa wajib menanggung seluruh biaya perbaikan di pusat servis resmi.</li>
                    <li><strong>Kehilangan:</strong> Penyewa wajib mengganti unit yang hilang dengan unit yang sama atau membayar biaya penggantian sesuai harga pasar yang berlaku saat itu.</li>
                </ul>

                <p class="mt-8">Dengan melakukan transaksi sewa, Anda dianggap telah membaca, memahami, dan menyetujui seluruh Kebijakan Pengembalian & Denda ini.</p>
